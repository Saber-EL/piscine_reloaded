#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*dest;
	int		l;

	l = 0;
	while (src[l])
		l++;
	dest = (char *)malloc(sizeof(char) * (l + 1));
	return (ft_strcpy(dest, src));
}
